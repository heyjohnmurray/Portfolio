						<h5>Be flexible in your communication</h5>
						<p>While I'm a firm believer in over-communication when necessary, sometimes that can get tiring. Learn to change things up when your communication isn't resonating with folks.</p>
						<p>Early on I was constantly being asked what design was going to deliver and when. Finally, I started to create a quarterly design brief in GitHub. I made it my top epic, and made all other epics dependencies of that parent epic. Then I shared out that link to the development leads and product management. Problem solved. Now I create a brief for every release and stakeholders have a better since of what my team is doing and delivering.</p>
