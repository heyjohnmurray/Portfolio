						<h4>Growing Pains and Learnings</h4>
						<p>As productive as our team has been in the six months since we've started working together, it hasn't been without its share of setbacks. Here's a brief list of things I've learned along the way.</p>
						
						<h5>Development restrictions impact design</h5>
						<p>You live and die by development's implementation schedule. No matter how much design work you hand over, if they hit an internal obstacle you may not see your work ship in the given release.</p>
						<p>Secondly, keep in mind that a portion of your design resources will be dedicated to overseeing developmental implementation. So even though you want to hand off and move on to working on the next release, you're still going to need to remain diligent. Otherwise, you'll run into inconsistencies between the designs and the finished, shipped product.</p>
					
						<hr>

						<h5>Shared alignment with stakeholders and remote teams</h5>
						<p>When you have remote teams, you can't bring folks in to do Google-style design sprints. That simply doesn't work at IBM. I've learned to break up design sprints into smaller remote collaboration and ideation sessions. Every session comes with a scheduled review meeting where the output is shared with the larger team.</p>
						<p><img src="/assets/images/screenshots/ibm-microclimate/process/ux-playback-process.png" alt=""></p>
						<small>Notice in the image above that there are "playback" meetings scheduled all along the way.</small>
						<hr>

						<h5>Establishing UX process is not instantaneous, it's progressive</h5>
						<p>I've <a href="https://medium.com/design-ibm/build-experiences-not-features-part-2-of-2-8d1c7ac523db">written extensively</a> on UX process and how to integrate design thinking with agile development. But showing the value of design and getting it a seat at the table take time. It's a slow and steady process and it's not something that is acheived in a single release or a single quarter. It's about refinement.</p>
						<p>In fact, my design team started out by having to meet short deadlines and didn't get to do as much ideation as we wanted, but over time we've been able to create the autonomy we need to explore. We definitely had growing pains with our stakeholders and with ourselves as a team.</p>
						<p>We were a textbook example of <a href="https://en.wikipedia.org/wiki/Tuckman%27s_stages_of_group_development">Tuckman's stages of group development:</a></p>
						<ul class="list">
							<li>Forming - our beta release</li>
							<li>Storming - our GA release</li>
							<li>Norming - our current release</li>
							<li>Performing - our next release, hopefully</li>
						</ul>
						<p>Over time we've evolved our process to what you see below.</p>
