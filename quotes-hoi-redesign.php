						<h1>Rethinking Everything</h1>
						<p>If it's not broken then why fix it? Besides the fact that the old design looked outdated, the codebase was hacked together from a previous coder who worked on the site. As a result, the site needed to be redone from the ground up on the code side. So it seemed like a great time to rethink the design as well.</p>

						<p>Beating this existing design was a frustrating process. Previous attempts to do so continued to fail, which made it hard for us to update the code. We couldn't just turn our back on a winning experience regardless of the code. So we figured the best way to beat the control was to study it and find a way to beat it at its own game.</p>

						<p>In the end, I reduced the dual entry to a single entry. This steamlined the process and made it clearer to the user what was expected of them. I used the other side, where the other entry used to be, to provide valuable content to encourage the user to enter their ZIP code.</p>
